    private void botaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoActionPerformed
       if (botaoClicado) {
        return;
    }
        
        modelList.addColumn("Numeração");
        modelList.addColumn("Titulo");
        modelList.addColumn("Autor");
        modelList.addColumn("Categoria");
        modelList.addColumn("Ano");
        
        for(ModelLivros Listar : Livros) {
            modelList.addRow(new Object[] {
                Listar.getIndice(),Listar.getAutor(),Listar.getTitulo(),Listar.getCategoria(),Listar.getAno()
            });
        }
        tabela.setModel(modelList);
        tabela.setDefaultEditor(Object.class, null);
            botaoClicado = true;
            botao.setEnabled(false);
    }//GEN-LAST:event_botaoActionPerformed
    
    private void comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprarActionPerformed
    String indiceText = indice.getText();
        if (indiceText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um índice válido");
            return;
        }        
    String indiceD = indice.getText();

    for (ModelLivros livro : Livros) {
        if (livro.getIndice().equals(indiceD)) {
            Livros.remove(livro);
            JOptionPane.showMessageDialog(this, "Livro comprado");
            this.dispose();
            break;
        } else {
            JOptionPane.showMessageDialog(this, "Livro não encontrado");
            }
        }
    }//GEN-LAST:event_comprarActionPerformed

