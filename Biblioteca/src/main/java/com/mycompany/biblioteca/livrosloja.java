    private void alugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alugarActionPerformed
            String indiceText = indice.getText();
        if (indiceText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um índice válido");
            return;
        }        
    String indiceD = indice.getText();
    
    for (ModelLivros livro : Livros) {
        if (livro.getIndice().equals(indiceD)) {
            LivrosAlugados.add(livro);
            Livros.remove(livro);
            JOptionPane.showMessageDialog(this, "Livro alugar");
            this.dispose();
            break;
        } else {
            JOptionPane.showMessageDialog(this, "Livro não encontrado");
        }
    }
    }//GEN-LAST:event_alugarActionPerformed

    private void devolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_devolverActionPerformed
              String indiceText = indice.getText();
        if (indiceText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um índice válido");
            return;
        }        
    String indiceD = indice.getText();
    
    for (ModelLivros livro : LivrosAlugados) {
        if (livro.getIndice().equals(indiceD)) {
            LivrosAlugados.remove(livro);
            Livros.add(livro);
            JOptionPane.showMessageDialog(this, "Livro devolvido");
            this.dispose();
            break;
        } else {
            JOptionPane.showMessageDialog(this, "Livro não encontrado");
        }
    }
    }//GEN-LAST:event_devolverActionPerformed
    
